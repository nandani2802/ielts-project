        		<p>Listening needs concentration, perseverance, purpose and understanding. Take a lot of IELTS practice tests which are readily available online and work on six essential skills which are required for the listening test.</p>
        		<ul style="color: white">
        			<li>Prediction -- When you look at the questions you should try to predict the answer before you hear it because this makes it easier to get the correct answer. You can practice this skill by reading the title and any other information related to the talk or conversation you are going to listen and predict some of the things you might hear. Write them down to check if your answers are correct. </li>
        			<li>Synonyms and Paraphrasing -- Listening test is also a vocabulary test. Many a time the answer will not be a direct match to the keywords in the question, but a synonym or a paraphrase of those words. Practice synonyms and paraphrasing by listening to a short video and think about how each sentence could be paraphrased. Practice this regularly! Over a period, you will get used to common synonyms which will help you in all parts of the IELTS test.</li>
        			<li>Connected Speech -- Connected Speech means linking of words and sounds together in a sentence. It is difficult to understand the sentence when it is not clear. Practice this by pausing a phrase you don t understand and replay it until you have followed all the words.</li>
        			<li>Recognizing Signposting Language -- The two recordings of the IELTS listening test will be monologues, one in a social context and one in a lecture style. When someone is talking in this way they often use what is referred to as signposting such as - this lecture is divided into three parts- "first of all", "secondly", "after that" and "finally". These phrases tell you what stage of the talk you are listening to and what they are going to say next. </li>
        			<li>Listening to Two People Talking -- Two of the four recordings of the listening test will be a discussion between two people. These people will have different accents, different ways of speaking and they might agree or disagree with each other. Listening to two people talking at the same time is a very difficult task, hence practice this by listening to the recordings of two people having a conversation. Try to note down the necessary details.</li>
