    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'name' => 'required',
            'email' => 'required',
            'pno' => 'required|numeric|digits:10',
            'city' => 'required',
            'message' => 'required'
        ]);
        $contact =  new contact([
            'cname' => $request->get('name'),
            'cemail' => $request->get('email'),
            'ccontact' => $request->get('pno'),
            'ccity' => $request->get('city'),
            'cdes' => $request->get('message')
        ]);
        $contact->save();
        //return give
        return redirect('/#contact')->with('success', 'Message sent Successfully!');
        //return redirect()->route('contact.index')->with('success','Data added');
    }

